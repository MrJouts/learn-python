# Tests for equality and inequality with strings
name = "Emiliano "
print(name == "emiliano")
print(name.lower() == "emiliano")
print(name.strip().lower() == "emiliano")

# Numbers
number = 10
print(number > 20)
print(number < 20)
print(number > 5 and number < 20)
print(number != 9 or number > 3)

# Lists
desserts = ["manzana", "lemon pie", "helado", "chocolate"]
print("manzana" in desserts)
print("limon" in desserts)
print("uvas" not in desserts)

