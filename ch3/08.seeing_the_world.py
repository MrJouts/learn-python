places = ["London", "New York", "Dubai", "Tokio", "Belgrado"]

# Lista original
print(places)
# Lista ordenada
print(sorted(places))
# Lista ordenada reverse
print(sorted(places, reverse=True))
# Lista reverse
places.reverse()
print(places)
places.reverse()
print(places)
# Lista sort
places.sort()
print(places)
places.sort(reverse=True)
print(places)